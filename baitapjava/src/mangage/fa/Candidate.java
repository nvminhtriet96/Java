package mangage.fa;

 class Candidate {
	 int id;
	 String name;
	 String gender;
	 String birthDate;
	 String email;
	 double gpa;

	 Candidate(int id, String name, String gender, String birthDate, String email, double gpa) {
		this.id = id;
		this.name = name;
		this.gender = gender;
		this.birthDate = birthDate;
		this.email = email;
		this.gpa = gpa;
	}
}
